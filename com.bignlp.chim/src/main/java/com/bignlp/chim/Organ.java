package com.bignlp.chim;

public class Organ {
	private String name;
	private String area;

	public Organ() {
		super();
	}

	public String getName() {
		return this.name;
	}

	public void setName(String argName) {
		this.name = argName;
	}

	public String getArea() {
		return this.area;
	}

	public void setArea(String argArea) {
		this.area = argArea;
	}

}
